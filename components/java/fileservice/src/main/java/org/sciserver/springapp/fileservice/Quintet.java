package org.sciserver.springapp.fileservice;

public class Quintet<X1, X2, X3, X4, X5> {

    public final X1 x1;
    public final X2 x2;
    public final X3 x3;
    public final X4 x4;
    public final X5 x5;

    public Quintet(X1 x1, X2 x2, X3 x3, X4 x4, X5 x5) {
        this.x1 = x1;
        this.x2 = x2;
        this.x3 = x3;
        this.x4 = x4;
        this.x5 = x5;
    }
}